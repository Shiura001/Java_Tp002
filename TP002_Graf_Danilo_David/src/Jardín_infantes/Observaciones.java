package Jardín_infantes;

/**
 *
 * @author Alumno
 */
public class Observaciones {
    Alumnos dni;
    private String nombre;
    private String apellido;
    private String profesor;
    private String desempeño;
    private String notas;
    private String personalidad;
    private String nivel_aprendizaje;

    public Alumnos getDni() {
        return dni;
    }

    public void setDni(Alumnos dni) {
        this.dni = dni;
    }

    public String getNombre() {
        return nombre;
    }

    public void setNombre(String nombre) {
        this.nombre = nombre;
    }

    public String getApellido() {
        return apellido;
    }

    public void setApellido(String apellido) {
        this.apellido = apellido;
    }

    public String getProfesor() {
        return profesor;
    }

    public void setProfesor(String profesor) {
        this.profesor = profesor;
    }

    public String getDesempeño() {
        return desempeño;
    }

    public void setDesempeño(String desempeño) {
        this.desempeño = desempeño;
    }

    public String getNotas() {
        return notas;
    }

    public void setNotas(String notas) {
        this.notas = notas;
    }

    public String getPersonalidad() {
        return personalidad;
    }

    public void setPersonalidad(String personalidad) {
        this.personalidad = personalidad;
    }

    public String getNivel_aprendizaje() {
        return nivel_aprendizaje;
    }

    public void setNivel_aprendizaje(String nivel_aprendizaje) {
        this.nivel_aprendizaje = nivel_aprendizaje;
    }
    
    
    
    
    
}
